<script>
  const slideData = {
    netvoice: {
      index: 0,
      images: ["nvproject.png", "nvproject2.png", "nvproject3.png"],
      intervalId: null
    },
    netsolar: {
      index: 0,
      images: ["nets.png", "nets2.png","nets3.png"],
      intervalId: null
    },
    emailsms: {
      index: 0,
      images: ["emailsms.png", "emailsms2.png", "emailsms3.png"],
      intervalId: null
    },
    smswebapp: {
      index: 0,
      images: ["smswebapp.png", "nvproject.png", "solarsavings.png"],
      intervalId: null
    },
    fuel: {
      index: 0,
      images: ["projectfuel1.png", "projectfuel2.png", "smart.png"],
      intervalId: null
    },
    smart: {
      index: 0,
      images: ["smart.png", "walt.png", "solarsavings.png"],
      intervalId: null
    },
    savings: {
      index: 0,
      images: ["solarsavings.png"],
      intervalId: null
    },
    waltermart: {
      index: 0,
      images: ["walt.png", "walt2.png"],
      intervalId: null
    }
  };

  function showSlide(id, index, isManual = false) {
    const data = slideData[id];
    data.index = index;
    document.getElementById(`main-slide-${id}`).src = data.images[index];

    const thumbs = document.querySelectorAll(`#thumbs-${id} img`);
    thumbs.forEach((img, i) => {
      img.classList.remove('border-green-500');
      img.classList.add('border-transparent');
      if (i === index) {
        img.classList.remove('border-transparent');
        img.classList.add('border-green-500');
      }
    });

    if (isManual) resetAutoSlide(id);
  }

  function changeSlide(id, direction, isManual = false) {
    const data = slideData[id];
    const total = data.images.length;
    data.index = (data.index + direction + total) % total;
    showSlide(id, data.index, isManual);
  }

  function startAutoSlide(id) {
    if (slideData[id].intervalId) clearInterval(slideData[id].intervalId);
    slideData[id].intervalId = setInterval(() => {
      changeSlide(id, 1);
    }, 5000); // every 5 seconds
  }

  function resetAutoSlide(id) {
    clearInterval(slideData[id].intervalId);
    startAutoSlide(id);
  }

  // Open modal and reset carousel to first image
  function openModal(id) {
    const modal = document.getElementById('modal-' + id);
    const content = modal.querySelector('div[onclick*="event.stopPropagation"]');
    modal.classList.remove('hidden');

    // Reset to first slide
    showSlide(id, 0, true);

    setTimeout(() => {
      content.classList.remove('opacity-0', 'scale-90');
      content.classList.add('opacity-100', 'scale-100');
    }, 10);
    document.body.classList.add('overflow-hidden');
  }

  // Close modal with animation
  function closeModal(id) {
    const modal = document.getElementById('modal-' + id);
    const content = modal.querySelector('div[onclick*="event.stopPropagation"]');
    content.classList.add('opacity-0', 'scale-90');
    content.classList.remove('opacity-100', 'scale-100');
    setTimeout(() => {
      modal.classList.add('hidden');
      document.body.classList.remove('overflow-hidden');
    }, 300);
  }

  // Start auto-slide on page load
  document.addEventListener("DOMContentLoaded", () => {
    Object.keys(slideData).forEach(id => {
      showSlide(id, 0);
      startAutoSlide(id);
    });
  });
</script>
